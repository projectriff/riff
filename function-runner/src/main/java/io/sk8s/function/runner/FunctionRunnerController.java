package io.sk8s.function.runner;

import java.util.function.Function;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RestController;

/**
 * @author Mark Fisher
 */
@RestController
public class FunctionRunnerController {

	@Autowired
	private Function<String, String> compiledFunction;

	@PostMapping
	public String post(@RequestBody String input) throws Exception {
		return this.compiledFunction.apply(input) + "\n";
	}
}
